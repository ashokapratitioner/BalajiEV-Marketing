// Auto-suggest scene from image color analysis (canvas, no API)
export const suggestSceneFromColors = (colorData, faceCount = 0) => {
  const { isBlueish, isGreenish, isWarm, isDark, brightness } = colorData;

  if (isWarm && faceCount >= 1)            return 'event';
  if (faceCount >= 2 && !isGreenish)       return 'ev_delivery';
  if (faceCount === 0 && isBlueish)        return 'new_arrival';
  if (isGreenish && brightness > 110)      return 'outdoor';
  if (isDark && faceCount >= 1)            return 'showroom';
  if (faceCount >= 3)                      return 'team';
  return 'ev_delivery';
};

// Analyse image colors using Canvas API (instant, offline)
export const analyzeImageColors = (imageUrl) =>
  new Promise((resolve) => {
    const img = new Image();
    img.onload = () => {
      const SIZE = 60;
      const canvas = document.createElement('canvas');
      canvas.width = SIZE; canvas.height = SIZE;
      const ctx = canvas.getContext('2d');
      ctx.drawImage(img, 0, 0, SIZE, SIZE);
      const data = ctx.getImageData(0, 0, SIZE, SIZE).data;

      let r = 0, g = 0, b = 0, count = 0;
      for (let i = 0; i < data.length; i += 4) {
        r += data[i]; g += data[i + 1]; b += data[i + 2]; count++;
      }
      r /= count; g /= count; b /= count;

      resolve({
        r, g, b,
        brightness: (r + g + b) / 3,
        isBlueish:  b > r * 1.15 && b > g * 1.05,
        isGreenish: g > r * 1.1  && g > b * 1.1,
        isWarm:     r > b * 1.25,
        isDark:     (r + g + b) / 3 < 100,
      });
    };
    img.onerror = () => resolve({ r:128, g:128, b:128, brightness:128, isBlueish:false, isGreenish:false, isWarm:false, isDark:false });
    img.src = imageUrl;
  });

